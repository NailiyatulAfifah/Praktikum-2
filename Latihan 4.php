<?php
  $a = 1;
  echo $a++."<br/>";
        //a dipanggil dulu baru ditambah
  echo $a."<br/>";
        //memanggil a + 1 = 2
  echo "<br>";

  $b = 1;
  echo ++$b."<br/>";
        //a ditambah dulu baru dipanggil
  echo $b."<br/>";
        //memanggil hasil akhir
  echo "<br>";

  $c = 2;
  echo $c--."<br/>";
        //a dipanggil dulu baru dikurang
  echo $c."<br/>";
        //memanggil a - 1 = 1
  echo "<br>";

  $d = 2;
  echo --$d."<br/>";
        //a dikurang dulu baru dipanggil
  echo $d."<br/>";
        //memanggil hasil akhir
  echo "<br>";
 ?>
